import re

data = "มาตรา 83 ผู้ใดขัดขวางการปฏิบัติหน้าที่ของพนักงานสำรวจตามมาตรา 28 หรือมาตรา 29 หรือของผู้บริหารท้องถิ่นหรือเจ้าหน้าที่ซึ่งผู้บริหารท้องถิ่นมอบหมายตามมาตรา 63 (3) หรือ (4) ต้องระวาง"
ref = [x for x in re.findall(r"มาตรา (\d+)", data) if int(x) != 83]
ref_commasep = ",".join(ref)
print(ref_commasep)
